using std::unique_ptr;

template<typename T, typename... Args>
core::enable_if_t<!core::is_array<T>::value, unique_ptr<T>> make_unique(Args&&... args) {
  return core::unique_ptr<T>(new T(core::forward<Args>(args)...));
}


